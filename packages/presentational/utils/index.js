/**
 *
 * @param {string|number} a
 * @param {string|number} b
 */
const add = (a, b) => a + b;
/**
 *
 * @param {number} a
 * @param {number} b
 */
const sub = (a, b) => a - b;
/**
 *
 * @param {number} a
 * @param {number} b
 */
const mult = (a, b) => a * b;
/**
 *
 * @param {number} a
 * @param {number} b
 */
const div = (a, b) => a / b;

module.exports = {
  add,
  sub,
  mult,
  div,
};
